    public static void GenerateTimelineElements()
    {
        string[] lines = recording.Split('\n');
        foreach (string line in lines)
        {
            string[] transformsElements = line.Split('_');
            // Debug.Log(transformsElements[0]);

            TimelineElement newElement = new TimelineElement();

            string posSVector = transformsElements[0].Substring(1, transformsElements[0].Length - 2);
            string[] posSVectorElements = posSVector.Split(',');
            Vector3 posVector = new Vector3(float.Parse(posSVectorElements[0]), float.Parse(posSVectorElements[1]), float.Parse(posSVectorElements[2]));
            newElement.Position = posVector;

            string sQuat = transformsElements[1].Substring(1, transformsElements[1].Length - 3);
            string[] sQuatElements = sQuat.Split(',');
            // Debug.Log(sQuatElements[0]);
            // Debug.Log(sQuatElements[1]);
            // Debug.Log(sQuatElements[2]);
            // Debug.Log(sQuatElements[3]);
            Quaternion quat = new Quaternion(float.Parse(sQuatElements[0].Trim()), float.Parse(sQuatElements[1].Trim()), float.Parse(sQuatElements[2].Trim()), float.Parse(sQuatElements[3].Trim()));
            newElement.Rotation = quat;

            elements.Add(newElement);
        }
    }
